#include (stdio.h)
int main()
{
    int n, ans=0;
    scanf("%d", &n);///用 scanf 讀輸入時，& 後面一定要是你想存輸入結果的變數名稱的位址。如果你要輸入 n，就用 &n。ans 通常是你程式裡用來計算、累積用的，不是用來存輸入。
///你讀入 ans 後，n 沒有被賦值，所以在 for 迴圈中 n 是未初始化的，這會導致錯誤行為。


    for (int i=1;i<=n;i++){ /// 迴圈從 i=0 開始加到 i <= n，這邊 i=0 加 0 沒問題，但通常求和會從 1 開始。
        ans += n; ///你寫成了 ans += n;，這樣每次迴圈都加的是 n，不是目前的 i，所以你得到的答案會是 n * n，不是 1 + 2 + 3 + ... + n
    }
    printf("%d",ans);
    return 0;
}
